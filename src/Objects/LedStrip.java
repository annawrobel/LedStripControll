public class LedStrip {
  private int red;
  private int green;
  private int blue;
  private int white;
  private double brightless;

  public int getRed() {
    return red;
  }

  public void setRed(int red) {
    this.red = red;
  }

  public int getGreen() {
    return green;
  }

  public void setGreen(int green) {
    this.green = green;
  }

  public int getBlue() {
    return blue;
  }

  public void setBlue(int blue) {
    this.blue = blue;
  }

  public int getWhite() {
    return white;
  }

  public void setWhite(int white) {
    this.white = white;
  }

  public double getBrightless() {
    return brightless;
  }

  public void setBrightless(double brightless) {
    this.brightless = brightless;
  }

}
